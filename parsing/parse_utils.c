#include "../msh.h"

int	is_white_space(char c)
{
	if ((c <= 13 && c >= 9) || c == ' ')
		return (1);
	return (0);
}

int	is_delimiter(char *str, int i)
{
	if (str[i] == '|' || str[i] == '>' || str[i] == '<' \
	|| (str[i] == '>' && str[i + 1] == '>') \
	|| (str[i] == '<' && str[i + 1] == '<'))
		return (1);
	return (0);
}
